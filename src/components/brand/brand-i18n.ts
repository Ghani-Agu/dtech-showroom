/** Keeps Western digits everywhere (Algeria uses them). */
export function fmtNum(n: number, lang: BrandLang): string {
  return n.toLocaleString(lang === 'en' ? 'en-US' : 'fr-FR')
}

/** Translate key `k` for `lang`, falling back to FR then the key itself. */
export function brandT(lang: BrandLang, k: string): string {
  return (BRAND_TR[lang] && BRAND_TR[lang][k]) || BRAND_TR.fr[k] || k
}
